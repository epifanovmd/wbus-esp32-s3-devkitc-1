#define CMD_READ_INFO_CUSTOMER_ID "F4 03 51 07 A1" // Чтение информации - ID клиента (партномер)
#define CMD_READ_INFO_SERIAL_NUMBER "F4 03 51 09 AF" // Чтение серийного номера и кода исполнительного стенда
